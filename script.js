// Initialize the Application
document.addEventListener('DOMContentLoaded', () => {
    // 1. Setup UI Toggle for Mobile Menu
    const menuToggle = document.getElementById('mobile-menu');
    const navMenuDesktop = document.querySelector('.nav-menu');
    const navMenuMobile = document.getElementById('mobileSectionsMenu');
    
    const overlay = document.getElementById('sidebarOverlay');
    
    const toggleMenu = () => {
        if(window.innerWidth <= 950) {
            const isActive = navMenuMobile.classList.toggle('active');
            if (overlay) overlay.classList.toggle('active');
            document.body.style.overflow = isActive ? 'hidden' : '';
        } else {
            navMenuDesktop.classList.toggle('active');
        }
    };

    const closeSidebar = document.getElementById('close-sidebar');
    if (menuToggle) menuToggle.addEventListener('click', toggleMenu);
    if (closeSidebar) closeSidebar.addEventListener('click', toggleMenu);
    if (overlay) overlay.addEventListener('click', toggleMenu);

    // 2. Render Initial Grids
    renderHomeSection();
    renderGrid('movies-grid', moviesData);
    renderGrid('foreign-movies-grid', foreignMoviesData);
    renderGrid('cartoon-movies-grid', cartoonMoviesData);
    renderGrid('series-grid', seriesData);
    renderGrid('foreign-series-grid', foreignSeriesData);
    renderGrid('live-grid', liveData);

    renderGrid('latest-list', latestData);

    // 3. Setup Search Bar functionality
    const searchInput = document.getElementById('searchInput');
    if (searchInput) {
        searchInput.addEventListener('keypress', function (e) {
            if (e.key === 'Enter') {
                handleSearch('searchInput');
            }
        });
    }

    const mobileSearchInput = document.getElementById('mobileSearchInput');
    if (mobileSearchInput) {
        mobileSearchInput.addEventListener('keypress', function (e) {
            if (e.key === 'Enter') {
                handleSearch('mobileSearchInput');
            }
        });
    }
    
    // Change Navbar style on scroll
    window.addEventListener('scroll', () => {
        const nav = document.querySelector('.navbar');
        if (window.scrollY > 50) {
            nav.style.backgroundColor = 'rgba(11, 16, 30, 0.98)';
            nav.style.boxShadow = '0 4px 10px rgba(0,0,0,0.5)';
        } else {
            nav.style.backgroundColor = 'rgba(11, 16, 30, 0.85)';
            nav.style.boxShadow = 'none';
        }
    });

    // Handle browser back/forward buttons
    window.addEventListener('popstate', (e) => {
        const pageId = (e.state && e.state.page) ? e.state.page : 'home';
        navigate(pageId, true);
    });

    // Handle initial URL hash on load
    const initialPage = window.location.hash.replace('#', '') || 'home';
    navigate(initialPage);
});

// Primary Navigation Logic
function navigate(pageId, fromPopstate = false) {
    // Push state and update hash
    if (!fromPopstate) {
        history.pushState({ page: pageId }, '', '#' + pageId);
    }

    // 1. Update Sections Visibility
    const sections = document.querySelectorAll('.page-section');
    sections.forEach(section => {
        section.classList.remove('active');
        // Optional: stop animation if needed
    });

    const targetEl = document.getElementById(pageId);
    if (targetEl) {
        targetEl.classList.add('active');
        window.scrollTo({ top: 0, behavior: 'smooth' });
    }

    // 2. Clear video if leaving watch page
    if (pageId !== 'watch') {
        const videoPlayer = document.getElementById('video-player');
        if (videoPlayer) videoPlayer.src = '';
    }

    // 3. Update Navbar Links Active State
    const navLinks = document.querySelectorAll('.nav-menu a, .logo');
    navLinks.forEach(link => {
        link.classList.remove('active');
        const onclickAttr = link.getAttribute('onclick');
        if (onclickAttr && onclickAttr.includes(`'${pageId}'`)) {
            link.classList.add('active');
        }
    });

    // 4. Close mobile menu if open
    const navMenuMobile = document.getElementById('mobileSectionsMenu');
    const navMenuDesktop = document.querySelector('.nav-menu');
    const overlay = document.querySelector('.menu-overlay');
    if (navMenuMobile) navMenuMobile.classList.remove('active');
    if (navMenuDesktop) navMenuDesktop.classList.remove('active');
    if (overlay) overlay.classList.remove('active');
    document.body.style.overflow = '';
}

function navigateBack() {
    // Use native browser history - works with keyboard shortcuts & swipe too
    if (history.length > 1) {
        history.back();
    } else {
        navigate('home');
    }
}

// Generate the Generic Grid HTML
function renderGrid(containerId, data) {
    const container = document.getElementById(containerId);
    if(!container) return;
    container.innerHTML = '';

    data.forEach(item => {
        const isSeries = item.type === 'series';
        // For series, show a generic badge or season count
        const badgeHTML = isSeries ? `<span class="badge">مسلسل | ${item.seasons.length} مواسم</span>` : '';
        const card = document.createElement('div');
        card.className = 'card';
        card.innerHTML = `
            <div class="card-img" onclick="watchItem('${item.id}')">
                ${badgeHTML}
                <img src="${item.poster}" alt="${item.title}">
                <div class="card-overlay">
                    <i class="fas fa-play-circle play-icon"></i>
                </div>
            </div>
            <div class="card-info">
                <h3 class="card-title" title="${item.title}">${item.title}</h3>
                <div class="card-meta">
                    <span>${item.year}</span>
                    <span style="color: var(--primary-color); font-weight: bold;">${item.quality}</span>
                </div>
                <div class="card-actions">
                    <button class="btn btn-watch" onclick="watchItem('${item.id}')">
                        <i class="fas fa-play"></i> شاهد
                    </button>
                    ${(!isSeries && item.downloads !== null) ? `
                    <a href="${item.downloads ? item.downloads.high : item.videoUrl}" target="_blank" class="btn btn-download">
                        <i class="fas fa-download"></i> تحميل
                    </a>` : ''}
                </div>
            </div>
        `;
        container.appendChild(card);
    });
}

