// Show/Hide section based on data availability
function renderSectionPreview(gridId, headerId, data) {
    const header = document.getElementById(headerId);
    const grid = document.getElementById(gridId);
    if (!header || !grid) return;
    if (data.length === 0) {
        header.style.display = 'none';
        grid.style.display = 'none';
    } else {
        renderGrid(gridId, data.slice(0, 4));
    }
}

// Find Series by ID or Search its episodes
function findSeriesByChildId(childId) {
    for (const s of [...seriesData, ...foreignSeriesData]) {
        for (const season of s.seasons) {
            if (season.episodes.find(ep => ep.id === childId)) {
                return s;
            }
        }
    }
    return null;
}

// Play Video & Watch Metadata
function watchItem(id, episodeId = null) {
    // 1. Try to find if 'id' is a direct item (movie or series object)
    let item = allData.find(i => i.id === id);
    let seriesObj = null;
    let currentEpisode = null;
    let currentSeasonNum = 1;

    if (item && item.type === 'series') {
        seriesObj = item;
        // If they just clicked the series from grid, default to S1 E1
        if (!episodeId) {
            currentSeasonNum = seriesObj.seasons[0].seasonNumber;
            currentEpisode = seriesObj.seasons[0].episodes[0];
        } else {
            // Find specific episode in seasons
            for (const season of seriesObj.seasons) {
                const foundEp = season.episodes.find(ep => ep.id === episodeId);
                if (foundEp) {
                    currentEpisode = foundEp;
                    currentSeasonNum = season.seasonNumber;
                    break;
                }
            }
        }
    } else if (!item) {
        // Might be a direct episode ID call
        seriesObj = findSeriesByChildId(id);
        if (seriesObj) {
            item = seriesObj;
            for (const season of seriesObj.seasons) {
                const foundEp = season.episodes.find(ep => ep.id === id);
                if (foundEp) {
                    currentEpisode = foundEp;
                    currentSeasonNum = season.seasonNumber;
                    break;
                }
            }
        }
    }

    if (!item) return;

    const videoPlayer = document.getElementById('video-player');
    const watchInfo = document.getElementById('watch-info');

    if(videoPlayer && watchInfo) {
        // Scroll to top
        window.scrollTo({ top: 0, behavior: 'smooth' });

        const isMovie = item.type === 'movie';
        let displayItem = isMovie ? item : currentEpisode;
        
        // If movie has versions, default to first version for player and initial links
        if (isMovie && item.versions && item.versions.length > 0) {
            displayItem = item.versions[0];
        }
        
        videoPlayer.src = displayItem.videoUrl;

        const epTitleExtra = !isMovie ? ` | الموسم ${currentSeasonNum} | ${displayItem.title}` : '';
        
        let downloadsHTML = '';
        const downloads = displayItem.downloads || null;
        if (downloads) {
            downloadsHTML = `
                <a href="${downloads.medium}" target="_blank" class="btn btn-download" style="display: inline-flex; width: auto; padding: 1rem 2rem; font-size: 1.1rem; border-radius: 30px;">
                    <i class="fas fa-download"></i> تحميل جودة متوسطة
                </a>
                <a href="${downloads.high}" target="_blank" class="btn btn-watch" style="display: inline-flex; width: auto; padding: 1rem 2rem; font-size: 1.1rem; border-radius: 30px;">
                    <i class="fas fa-download"></i> جودة عالية
                </a>
            `;
        } else {
            downloadsHTML = `
                <a href="${displayItem.videoUrl}" target="_blank" class="btn btn-watch" style="display: inline-flex; width: auto; padding: 1rem 2.5rem; font-size: 1.1rem; border-radius: 30px;">
                    <i class="fas fa-download"></i> تحميل الان
                </a>
            `;
        }

        // Generate Series Navigation
        let seriesNavHTML = '';
        if (item.type === 'series') {
            seriesNavHTML = getSeriesNavigationHTML(item, currentEpisode.id, currentSeasonNum);
        }

        // Generate Versions HTML if available
        let versionsHTML = '';
        if (item.versions && item.versions.length > 0) {
            versionsHTML = `
                <div class="series-nav-container" style="margin-bottom: 2rem;">
                    <div class="nav-header">
                        <h3 style="color: #fff; display: flex; align-items: center; gap: 0.5rem;">
                            <i class="fas fa-closed-captioning" style="color: var(--primary-color);"></i> اختر النسخة
                        </h3>
                    </div>
                    <div class="episodes-grid" style="grid-template-columns: repeat(auto-fill, minmax(120px, 1fr));">
                        ${item.versions.map((v, index) => `
                            <button class="episode-btn ${index === 0 ? 'active' : ''}" onclick="switchMovieVersion('${item.id}', ${index}, this)">
                                ${v.name}
                            </button>
                        `).join('')}
                    </div>
                </div>
            `;
        }

        // Generate Parts Navigation HTML if available
        let partsHTML = '';
        if (item.parts && item.parts.length > 0) {
            partsHTML = `
                <div class="series-nav-container" style="margin-bottom: 2rem; border-color: #eab308;">
                    <div class="nav-header">
                        <h3 style="color: #fff; display: flex; align-items: center; gap: 0.5rem;">
                            <i class="fas fa-layer-group" style="color: #eab308;"></i> أجزاء الفيلم
                        </h3>
                    </div>
                    <div class="episodes-grid" style="grid-template-columns: repeat(auto-fill, minmax(120px, 1fr));">
                        ${item.parts.map(p => `
                            <button class="episode-btn ${p.id === item.id ? 'active' : ''}" 
                                    onclick="watchItem('${p.id}')" 
                                    style="${p.id === item.id ? 'background: #eab308; border-color: #eab308;' : ''}">
                                ${p.name}
                            </button>
                        `).join('')}
                    </div>
                </div>
            `;
        }

        watchInfo.innerHTML = `
            <h2 class="watch-title">${item.title}${epTitleExtra}</h2>
            <div class="watch-badges">
                <span class="badge-quality">${item.quality}</span>
                <span style="color: var(--text-muted); font-size: 1.1rem;">${item.year}</span>
                <span style="color: var(--primary-color); border: 1px solid var(--primary-color); padding: 0.2rem 0.6rem; border-radius: 4px;">${isMovie ? 'فيلم' : 'مسلسل'}</span>
            </div>
            
            ${partsHTML}
            ${versionsHTML}
            ${seriesNavHTML}

            <p class="watch-desc">${item.desc}</p>
            
            ${item.downloads !== null ? `
            <div class="download-bar" id="download-section">
                <h3 style="margin-bottom: 1rem; color: #fff;">خيارات التحميل:</h3>
                <div style="display: flex; flex-wrap: wrap; gap: 1rem;" id="download-links">
                    ${downloadsHTML}
                </div>
            </div>
            ` : ''}
        `;

        navigate('watch');
    }
}

// Switch between Movie Versions (Subbed/Dubbed)
function switchMovieVersion(itemId, versionIndex, btnEl) {
    const item = allData.find(i => i.id === itemId);
    if (!item || !item.versions) return;
    
    const version = item.versions[versionIndex];
    const videoPlayer = document.getElementById('video-player');
    const downloadLinks = document.getElementById('download-links');
    
    if (videoPlayer) videoPlayer.src = version.videoUrl;
    
    // Update active button
    const buttons = btnEl.parentElement.querySelectorAll('.episode-btn');
    buttons.forEach(b => b.classList.remove('active'));
    btnEl.classList.add('active');
    
    // Update downloads
    if (downloadLinks) {
        let downloadsHTML = '';
        if (version.downloads) {
            downloadsHTML = `
                <a href="${version.downloads.medium}" target="_blank" class="btn btn-download" style="display: inline-flex; width: auto; padding: 1rem 2rem; font-size: 1.1rem; border-radius: 30px;">
                    <i class="fas fa-download"></i> تحميل جودة متوسطة
                </a>
                <a href="${version.downloads.high}" target="_blank" class="btn btn-watch" style="display: inline-flex; width: auto; padding: 1rem 2rem; font-size: 1.1rem; border-radius: 30px;">
                    <i class="fas fa-download"></i> جودة عالية
                </a>
            `;
        } else {
            downloadsHTML = `
                <a href="${version.videoUrl}" target="_blank" class="btn btn-watch" style="display: inline-flex; width: auto; padding: 1rem 2.5rem; font-size: 1.1rem; border-radius: 30px;">
                    <i class="fas fa-download"></i> تحميل الان
                </a>
            `;
        }
        downloadLinks.innerHTML = downloadsHTML;
    }
}

// Logic for Series Navigation
function getSeriesNavigationHTML(seriesObj, currentEpisodeId, currentSeasonNum) {
    const seasons = seriesObj.seasons;
    const currentSeason = seasons.find(s => s.seasonNumber === currentSeasonNum);
    
    // Find flattened episodes to determine prev/next across seasons
    const allEpisodes = [];
    seasons.forEach(s => {
        s.episodes.forEach(ep => {
            allEpisodes.push({ ...ep, seasonNum: s.seasonNumber });
        });
    });

    const currentIndex = allEpisodes.findIndex(e => e.id === currentEpisodeId);
    const prevEp = allEpisodes[currentIndex - 1];
    const nextEp = allEpisodes[currentIndex + 1];

    const seasonList = seasons.map(s => s.seasonNumber).sort((a,b) => a-b);

    let html = `
        <div class="series-nav-container">
            <div class="nav-header">
                <h3 style="color: #fff; display: flex; align-items: center; gap: 0.5rem;">
                    <i class="fas fa-list-ul" style="color: var(--primary-color);"></i> حلقات المسلسل
                </h3>
                <div class="episode-controls">
                    <button class="btn-nav" onclick="watchItem('${seriesObj.id}', '${prevEp ? prevEp.id : ''}')" ${!prevEp ? 'disabled' : ''}>
                        <i class="fas fa-chevron-right"></i> السابقة
                    </button>
                    <button class="btn-nav next" onclick="watchItem('${seriesObj.id}', '${nextEp ? nextEp.id : ''}')" ${!nextEp ? 'disabled' : ''}>
                        التالية <i class="fas fa-chevron-left"></i>
                    </button>
                </div>
            </div>

            <div class="seasons-scroll">
                ${seasonList.map(sNum => `
                    <div class="season-btn ${sNum === currentSeasonNum ? 'active' : ''}" 
                            onclick="switchSeasonDisplay(${sNum}, '${currentEpisodeId}', '${seriesObj.id}')">
                        الموسم ${sNum}
                    </div>
                `).join('')}
            </div>

            <div class="episodes-grid" id="episodes-listing">
                ${currentSeason.episodes.map(ep => `
                    <div class="episode-btn ${ep.id === currentEpisodeId ? 'active' : ''}" 
                         onclick="watchItem('${seriesObj.id}', '${ep.id}')">
                        ${ep.title}
                    </div>
                `).join('')}
            </div>
        </div>
    `;

    return html;
}

// Switch season view
function switchSeasonDisplay(seasonNum, currentActiveId, seriesId) {
    const seriesObj = [...seriesData, ...foreignSeriesData].find(s => s.id === seriesId);
    if (!seriesObj) return;

    const episodesListing = document.getElementById('episodes-listing');
    const seasonBtns = document.querySelectorAll('.season-btn');
    
    // Update active button
    seasonBtns.forEach(btn => {
        btn.classList.remove('active');
        if (btn.innerText.includes(seasonNum)) {
            btn.classList.add('active');
        }
    });

    const targetSeason = seriesObj.seasons.find(s => s.seasonNumber === seasonNum);
    if (!targetSeason) return;

    episodesListing.innerHTML = targetSeason.episodes.map(ep => `
        <div class="episode-btn ${ep.id === currentActiveId ? 'active' : ''}" 
             onclick="watchItem('${seriesObj.id}', '${ep.id}')">
            ${ep.title}
        </div>
    `).join('');
}

// Search Logic
function handleSearch(inputId = 'searchInput') {
    const searchInput = document.getElementById(inputId);
    if (!searchInput) return;

    const query = searchInput.value.trim().toLowerCase();
    if (query === '') return;

    // Search in movies and series titles
    const results = allData.filter(item => item.title.toLowerCase().includes(query));
    
    const container = document.getElementById('search-grid');
    if(container) {
        if (results.length > 0) {
            renderGrid('search-grid', results);
        } else {
            container.innerHTML = `
                <div style="text-align: center; grid-column: 1 / -1; padding: 4rem 0;">
                    <i class="fas fa-search" style="font-size: 4rem; color: var(--outline-color); margin-bottom: 1rem;"></i>
                    <h3 style="color: var(--text-muted); font-size: 1.5rem;">عفواً، لا توجد نتائج مطابقة لبحثك عن "${query}"</h3>
                </div>
            `;
        }
        
        // Clear inputs after search
        document.getElementById('searchInput').value = '';
        const mInput = document.getElementById('mobileSearchInput');
        if(mInput) mInput.value = '';

        navigate('search-results');
    }
}
