// Robust sorting for dates like "11 أبريل 2026" or just "2019"
const latestData = [...allData].sort((a, b) => {
    let scoreA = 0, scoreB = 0;
    if (a.year && typeof a.year === 'string') {
        let matchA = a.year.match(/(\d+)\s+([^\s]+)\s+(\d{4})/);
        if (matchA) scoreA = parseInt(matchA[3]) * 10000 + (monthNames[matchA[2]] || 0) * 100 + parseInt(matchA[1]);
        else scoreA = (parseInt((a.year.match(/\d{4}/) || [0])[0]) * 10000) || 0;
    }
    if (b.year && typeof b.year === 'string') {
        let matchB = b.year.match(/(\d+)\s+([^\s]+)\s+(\d{4})/);
        if (matchB) scoreB = parseInt(matchB[3]) * 10000 + (monthNames[matchB[2]] || 0) * 100 + parseInt(matchB[1]);
        else scoreB = (parseInt((b.year.match(/\d{4}/) || [0])[0]) * 10000) || 0;
    }
    return scoreB - scoreA;
}).slice(0, 4);

